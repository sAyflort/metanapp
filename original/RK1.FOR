      IF(NCORD.EQ.2) A(IC2,J,NF)=ROS*VINS*(R(J)-R(JA2))*0.5*
     * (R(J)+R(JA2))
      IF(NCORD.EQ.1)
     * A(IC2,J,NF)= A(IC2,JA2,NF)-ROS*VINS*(X2(JA2 )-X2(J))
   64 CONTINUE
      A(IC2,JA2,NV1)=0.
      DO 63 J=JA3,JA1
      A(IC2,J,NF)=A(IC2,JA1,NF)
  63  CONTINUE
      DO 21 I=1,IC2
