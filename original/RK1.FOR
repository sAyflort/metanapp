      A(1,J,NV1)=VINP
      A(1,J,NMU2)=VINP*R(J)**2/R(JA)*1.5
      A(1,J,NMOX1)=0.0
      A(1,J,NMFU1)=ZMF2N
      A(1,J,NRO3)=ROP
      A(1,J,NT)=298.
      A(1,J,NMU1)=H
