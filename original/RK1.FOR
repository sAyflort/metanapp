      DO 20 J=JAA,JN
      IMIN(J)=IC2+1
   20 CONTINUE
      DO 16 J=1,JC
      IMAX(J)=IN-1
   16 CONTINUE
      DO 17 J=JC1,JN
      IMAX(J)=IC-1+JC-J
   17 CONTINUE
      DO 64 J=JA1,JA2
